package intraday;

public class DayData
{
	String high;
	String low;
	String closed;

	public String getHigh()
	{
		return high;
	}

	public void setHigh(String high)
	{
		this.high = high;
	}

	public String getLow()
	{
		return low;
	}

	public void setLow(String low)
	{
		this.low = low;
	}

	public String getClosed()
	{
		return closed;
	}

	public void setClosed(String closed)
	{
		this.closed = closed;
	}
}
